// number typeof conversion
let age = 30;
console.log(typeof(age))

// string
let username = "Dijkstra123";
console.log(typeof(username))

// boolean
let isAlive = true;
console.log(typeof(isAlive))

// null
let illness = null;
console.log(typeof(illness))

// undefined
let gf = undefined;
console.log(typeof(gf))

